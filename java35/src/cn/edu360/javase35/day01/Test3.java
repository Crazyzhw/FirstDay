package cn.edu360.javase35.day01;

public class Test3 {

	public static void main(String[] args) {
		int i = 5;
		int j = 5;
		int m = 5;
		int n = 5;
		i++;//i=6
		j = j + 1;//j=6
		m--;//m=4
		n = n - 1;//n=4
		System.out.println(i);
		System.out.println(i++);
		System.out.println(++i);
		System.out.println(i--);
		System.out.println(i);
		System.out.println(j);
		System.out.println(j++);
		System.out.println(j--);
		System.out.println(--j);
		System.out.println(j);
		System.out.println(m);
		System.out.println(n);

	}

}
